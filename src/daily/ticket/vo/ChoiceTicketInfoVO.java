package daily.ticket.vo;

public class ChoiceTicketInfoVO {
	
	private String seatCode      ;
	private String ticketTime    ;
	private String ticketPrice      ;
	private String ticketType    ;
	private String ticketTypeTime   ;
	public String getSeatCode() {
		return seatCode;
	}
	public void setSeatCode(String seatCode) {
		this.seatCode = seatCode;
	}
	public String getTicketTime() {
		return ticketTime;
	}
	public void setTicketTime(String ticketTime) {
		this.ticketTime = ticketTime;
	}
	public String getTicketPrice() {
		return ticketPrice;
	}
	public void setTicketPrice(String ticketPrice) {
		this.ticketPrice = ticketPrice;
	}
	public String getTicketType() {
		return ticketType;
	}
	public void setTicketType(String ticketType) {
		this.ticketType = ticketType;
	}
	public String getTicketTypeTime() {
		return ticketTypeTime;
	}
	public void setTicketTypeTime(String ticketTypeTime) {
		this.ticketTypeTime = ticketTypeTime;
	}
	
	
	
}
